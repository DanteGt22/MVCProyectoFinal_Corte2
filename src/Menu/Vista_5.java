    private void Suma(int num){
        if(num > 0){
            cantidadPos++;
            sumPos=sumPos+num;
        }
        if(num == 0){
            cantidadNeu++;
        }
        if(num <0){
            cantidadNeg++;
            sumNeg=sumNeg+num; 
        }
    }
