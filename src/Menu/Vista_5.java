    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
            try{
            int num = Integer.valueOf(jTextField1.getText());
            jTextField1.setText("");
            Suma(num);
            count++;
            if(count+1>cantidadNum){
                
            JOptionPane.showMessageDialog(this,"Cantidad de positivos: "+cantidadPos+"."+"\n"
                    +"Cantidad de negativos: "+cantidadNeg+"."+"\n"
                    +"Cantidad de neutros: "+cantidadNeu+"."+"\n"
                    +"Suma de positivos: "+sumPos+"."+"\n"
                    +"Suma de negativos: "+sumNeg+"."+"\n");
            cantidadNeg=0;
            cantidadPos=0;
            cantidadNeu=0;
            sumPos=0;
            sumNeg=0;
            count=0;
            jLabel1.setVisible(true);
            jLabel2.setVisible(true);
            jTextField1.setText("");
            jButton2.setVisible(false);
            jButton1.setVisible(true);
            }
            }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this,"Por favor ingrese los datos correctamente!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

